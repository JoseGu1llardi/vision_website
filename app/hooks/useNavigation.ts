import { create } from "zustand";

type Page = "home" | "portfolio" | "contact";

interface NavigationStore {
  currentPage: Page;
  setPage: (page: Page) => void;
}

export const useNavigation = create<NavigationStore>()((set) => ({
  currentPage: "home",
  setPage: (page) => set({ currentPage: page }),
}));
